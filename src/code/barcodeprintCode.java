package code;

/**
 *
 * @author M_Samly
 */
public class barcodeprintCode {

    String date;
    String time;
    String barcode;
    String name;
    int qty;

    public barcodeprintCode(String date, String time, String barcode, String name, int qty) {
        this.date = date;
        this.time = time;
        this.barcode = barcode;
        this.name = name;
        this.qty = qty;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getBarcode() {
        return barcode;
    }

    public void setBarcode(String barcode) {
        this.barcode = barcode;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }
}
