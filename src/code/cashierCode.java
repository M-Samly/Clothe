package code;

/**
 *
 * @author M_Samly
 */
public class cashierCode {

    String barcode;
    String name;
    int qty;

    public cashierCode(String barcode, String name, int qty) {
        this.barcode = barcode;
        this.name = name;
        this.qty = qty;
    }

    public String getBarcode() {
        return barcode;
    }

    public void setBarcode(String barcode) {
        this.barcode = barcode;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }

}
