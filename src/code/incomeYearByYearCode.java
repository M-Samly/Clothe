public class incomeYearByYearCode {

    String year;
    float income;

    public incomeYearByYearCode(String year, float income) {
        this.year = year;
        this.income = income;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public float getIncome() {
        return income;
    }

    public void setIncome(float income) {
        this.income = income;
    }
 
}
