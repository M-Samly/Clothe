    public ArrayList<loanCode> loanList() {
        ArrayList<loanCode> loanList = new ArrayList<>();
        try {
            Connection con;
            Statement st;
            con = db.getConnection();
            st = con.createStatement();
            String query = "Select * from loan order by b_id";
            ResultSet rs = st.executeQuery(query);
            loanCode pro;
            while (rs.next()) {
                pro = new loanCode(rs.getInt("b_id"), rs.getString("date"), rs.getString("time"), rs.getString("customer"), rs.getInt("telephone"), rs.getString("nic"), rs.getString("status"), rs.getFloat("amount"));
                loanList.add(pro);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return loanList;
    }

    public void show_Loan() {
        ArrayList<loanCode> list = loanList();
        DefaultTableModel model = (DefaultTableModel) tblLoan.getModel();
        Object[] row = new Object[8];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getB_id();
            row[1] = list.get(i).getDate();
            row[2] = list.get(i).getTime();
            row[3] = list.get(i).getCustomer();
            row[4] = list.get(i).getTelephone();
            row[5] = list.get(i).getNic();
            row[6] = list.get(i).getStatus();
            row[7] = list.get(i).getAmount();
            model.addRow(row);
        }
    }

