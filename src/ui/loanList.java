    private void txtpayedKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpayedKeyReleased
        // TODO add your handling code here:
        float amount = Float.parseFloat(txtamount.getText());
        float payed = Float.parseFloat(txtpayed.getText());
        float balance = amount - payed;
        txtbalance.setText(String.valueOf(new DecimalFormat("0.00").format(balance)));
    }//GEN-LAST:event_txtpayedKeyReleased

    private void txtpayedKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpayedKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            float payed = Float.parseFloat(txtpayed.getText());
            float balance = Float.parseFloat(txtbalance.getText());
            int billno = Integer.parseInt(txtbill.getText());
            try {
                Connection con;
                PreparedStatement pst;
                con = db.getConnection();
                String query = "Update sales set payment = payment + " + payed + ", balance = balance + " + payed + " where b_id = " + billno + "";
                pst = con.prepareStatement(query);
                pst.executeUpdate();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            if (balance == 0) {
                try {
                    Connection con;
                    PreparedStatement pst;
                    con = db.getConnection();
                    String query = "delete from loan where b_id = " + billno + "";
                    pst = con.prepareStatement(query);
                    pst.executeUpdate();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            } else {
                try {
                    Connection con;
                    PreparedStatement pst;
                    con = db.getConnection();
                    String query = "Update loan set amount = amount - " + payed + " where b_id = " + billno + "";
                    pst = con.prepareStatement(query);
                    pst.executeUpdate();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }

            DefaultTableModel model = (DefaultTableModel) tblLoan.getModel();
            model.setRowCount(0);
            show_Loan();

            txtSearchBillNo.setText(null);
            txtSearchName.setText(null);
            txtbill.setText(null);
            txtcustomer.setText(null);
            txtamount.setText(null);
            txtpayed.setText(null);
            txtbalance.setText(null);
            txtSearchBillNo.requestFocus();
        }
    }//GEN-LAST:event_txtpayedKeyPressed

